// Adding `.length` to the end of array

// This will not list the contents of the array, but just the lengtgh of the array
console.log([99, 100, 101].length);

// This will list the contents of the array
console.log([99, 100, 101]);

// Adding `.length` on to a variable that contains
// an array works, too. Cool!
var letters = ["a", "b", "c"];
console.log(letters.length);


